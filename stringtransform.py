#string transform method
# capitalize
a = "who are you"
print(a.capitalize())

#title

a = "dhanunjai sai sundhar"
print(a.title())

#upper

a = "dhanunjai sai sundhar"
print(a.upper())

#lower

a = "DHANUNJAI SAI SUNDHAR"
print(a.lower())

#swap case
a = "sundhar"
print(a.swapcase())

#casefold
a = "DHANUNJAI SAI SUNDHAR"
print(a.casefold())



